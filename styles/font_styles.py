def generate_bio_style(text: str) -> list:
    """Generate fancy bio styles with decorations."""
    results = []
    for style_name in FONT_STYLES:
        styled_text = apply_style(text, style_name)
        for dec_style in ['stars', 'hearts', 'flowers', 'sparkles', 'crown', 'fancy', 'royal', 'dragon', 'crystal', 'bubble']:
            decorated_text = add_decorations(styled_text, dec_style)
            results.append({
                'style': f"{style_name}_{dec_style}",
                'text': decorated_text
            })
    return results 