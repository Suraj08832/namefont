def apply_style(text: str, style_name: str) -> str:
    """Apply a specific font style to the text."""
    if style_name not in FONT_STYLES:
        return text
        
    style = FONT_STYLES[style_name]
    return ''.join(style.get(c, c) for c in text)

def generate_fancy_name(text: str) -> list:
    """Generate text in all available fancy styles."""
    results = []
    for style_name in FONT_STYLES:
        styled_text = apply_style(text, style_name)
        results.append({
            'style': style_name,
            'text': styled_text
        })
    return results

def add_decorations(text: str, style: str = 'normal') -> str:
    """Add decorative elements around the text."""
    decorations = {
        'stars': (f'★彡 {text} 彡★', '⭐'),
        'hearts': (f'♥️ {text} ♥️', '❤️'),
        'flowers': (f'✿ {text} ✿', '🌸'),
        'sparkles': (f'✨ {text} ✨', '✨'),
        'crown': (f'👑 {text} 👑', '👑'),
        'fancy': (f'꧁ {text} ꧂', '✨'),
        'royal': (f'༺ {text} ༻', '👑'),
        'dragon': (f'𒆜 {text} 𒆜', '🐉'),
        'crystal': (f'❈ {text} ❈', '💎'),
        'bubble': (f'⊹ {text} ⊹', '🫧'),
    }
    
    if style in decorations:
        return decorations[style][0]
    return f"✨ {text} ✨"  # Default decoration

def generate_bio_style(text: str) -> list:
    """Generate fancy bio styles with decorations."""
    results = []
    for style_name in FONT_STYLES:
        styled_text = apply_style(text, style_name)
        for dec_style in ['stars', 'hearts', 'flowers', 'sparkles', 'crown', 'fancy', 'royal', 'dragon', 'crystal', 'bubble']:
            decorated_text = add_decorations(styled_text, dec_style)
            results.append({
                'style': f"{style_name}_{dec_style}",
                'text': decorated_text
            })
    return results 